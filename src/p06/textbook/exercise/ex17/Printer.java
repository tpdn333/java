package p06.textbook.exercise.ex17;

public class Printer {
	static void printer(int a) {
		System.out.println(a);
	}

	static void printer(boolean a) {
		System.out.println(a);
	}

	static void printer(double a) {
		System.out.println(a);
	}

	static void printer(String a) {
		System.out.println(a);
	}
}
