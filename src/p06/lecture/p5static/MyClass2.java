package p06.lecture.p5static;

public class MyClass2 {
	// instance member
	// instance field
	int j;
	
	// instance method
	void method1() {
		System.out.println("method not static");
	}
	
	// class(static) member
	// class field ( static field )
	static int i;
	
	// class method ( static method )
	static void method() {
		System.out.println("method static");
	}
	
}
